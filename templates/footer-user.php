<!-- JavaScript สำหรับการทำงานของ Footer -->
<script>
    // เพิ่มเอฟเฟกต์เมื่อเลื่อนเมาส์เหนือลิงก์
    document.addEventListener('DOMContentLoaded', function () {
        const footerLinks = document.querySelectorAll('footer a');

        footerLinks.forEach(link => {
            link.addEventListener('mouseenter', function () {
                this.style.transform = 'translateX(5px)';
            });

            link.addEventListener('mouseleave', function () {
                this.style.transform = 'translateX(0)';
            });
        });

        // เพิ่มการคลิกเพื่อเลื่อนขึ้นด้านบน
        const scrollToTop = document.createElement('button');
        scrollToTop.innerHTML = '<i class="fas fa-chevron-up"></i>';
        scrollToTop.className = 'btn btn-warning position-fixed rounded-circle';
        scrollToTop.style.cssText = `
        bottom: 20px;
        right: 20px;
        width: 50px;
        height: 50px;
        z-index: 1000;
        display: none;
    `;

        scrollToTop.addEventListener('click', function () {
            window.scrollTo({ top: 0, behavior: 'smooth' });
        });

        document.body.appendChild(scrollToTop);

        // แสดงปุ่มเมื่อเลื่อนลง
        window.addEventListener('scroll', function () {
            if (window.pageYOffset > 300) {
                scrollToTop.style.display = 'block';
            } else {
                scrollToTop.style.display = 'none';
            }
        });
    });
</script>